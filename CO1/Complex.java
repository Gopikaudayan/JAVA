                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                            import java.util.Scanner;
class Complex{
	public static void main(String args[]){
		System.out.println();
		System.out.println("...Program to add two complex numbers...");
		System.out.println();
		Scanner sc=new Scanner(System.in);
		
		System.out.println("Enter the real part of first number : ");
		int a=sc.nextInt();
		System.out.println("Enter the imaginary part of first number : ");
		int b=sc.nextInt();
		System.out.println("First complex number is : "+a+"+"+b+"i");
		
		System.out.println("Enter the real part of second number : ");
		int x=sc.nextInt();
		System.out.println("Enter the imaginary part of second number : ");
		int y=sc.nextInt();
		System.out.println("second complex number is : "+x+"+"+y+"i");
		
		int m=a+x;
		int n=b+y;
		System.out.println("Sum of the complex numbers is : "+m+"+"+n+"i");
		
	}
}